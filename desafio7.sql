SELECT UCASE(CONCAT(EM.FIRST_NAME, ' ', EM.LAST_NAME)) AS 'Nome completo',
JH.START_DATE AS 'Data de início', EM.SALARY AS 'Salário'
FROM hr.employees AS EM
INNER JOIN hr.job_history AS JH ON EM.EMPLOYEE_ID = JH.EMPLOYEE_ID
WHERE MONTH(JH.START_DATE) IN (01, 02 ,03)
ORDER BY `Nome completo`, JH.START_DATE;
