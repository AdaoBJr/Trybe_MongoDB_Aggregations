SELECT cargo.JOB_TITLE AS Cargo, ROUND(AVG(SALARY),2) AS "Média salarial",
CASE 
WHEN AVG(SALARY)  BETWEEN 2000 AND 5800 THEN 'Júnior'
WHEN AVG(SALARY)  BETWEEN 5801 AND 7500 THEN 'Pleno'
WHEN AVG(SALARY)  BETWEEN 7501 AND 10500 THEN 'Sênior'
ELSE 'CEO'
END AS `Senioridade` 

FROM employees AS info
LEFT JOIN jobs AS cargo
ON info.JOB_ID = cargo.JOB_ID
GROUP  BY info.JOB_ID
ORDER BY `Média salarial`, `Cargo`;
