SELECT JOB_TITLE AS 'Cargo',
ROUND(AVG(MIN_SALARY + MAX_SALARY), 2) AS `Média salarial`,
CASE
WHEN (ROUND(AVG(MIN_SALARY + MAX_SALARY), 2) >= 2000 AND ROUND(AVG(MIN_SALARY + MAX_SALARY), 2) <=5800) THEN ('Junior')
WHEN (ROUND(AVG(MIN_SALARY + MAX_SALARY), 2) >= 5801 AND ROUND(AVG(MIN_SALARY + MAX_SALARY), 2) <=7500) THEN ('Pleno')
WHEN (ROUND(AVG(MIN_SALARY + MAX_SALARY), 2) >= 7501 AND ROUND(AVG(MIN_SALARY + MAX_SALARY), 2) <=10500) THEN ('Sênior')
WHEN (ROUND(AVG(MIN_SALARY + MAX_SALARY), 2) > 10500) THEN ('CEO')
END AS 'Senioridade'
FROM hr.jobs
GROUP BY JOB_TITLE;
